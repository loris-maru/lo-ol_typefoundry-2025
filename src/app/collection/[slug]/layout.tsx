import type { Metadata } from "next";

import { notFound } from "next/navigation";

import { getAllTypefaces, getTypefaceBySlug } from "@/api/typefaces";
import CollectionNavigation from "@/ui/segments/global/navigation/collection-navigation";

export const metadata: Metadata = {
  title: "Fuzar Collection",
  description: "Generated by create next app",
};

export default async function RootLayout({
  children,
  params,
}: {
  children: React.ReactNode;
  params: Promise<{ slug: string }>;
}) {
  try {
    const { slug } = await params;
    const typeface = await getTypefaceBySlug(slug);
    const allTypefaces = await getAllTypefaces();

    if (!typeface) {
      notFound();
    }

    return (
      <div className="relative w-full">
        <CollectionNavigation content={typeface} allTypefaces={allTypefaces} />
        {children}
      </div>
    );
  } catch (error) {
    console.error("Failed to fetch typeface:", error);

    return (
      <div className="relative w-full p-8 text-center">
        <h1 className="text-2xl font-bold text-red-600">Error Loading Typeface</h1>
        <p className="mt-2 text-gray-600">Failed to load typeface data. Please try again later.</p>
      </div>
    );
  }
}
